package com.example;

public class Calc {

    public int add(int a,int b) {
        return a+b;
    }

    public int mul(int a,int b) {
        return a*b;
    }

    public int sub(int a,int b) {
        return a-b;
    }

    public int div(int a,int b) {
        return a/b;
    }

    public int fact(int a,int b) {
        return a/b*11;
    }

}
